def _normalize(name: str | None) -> str:
    if not name:
        return ""
    s = name.strip()
    # 去掉括号说明: "伦敦国王学院（KCL）" → "伦敦国王学院"
    for left, right in (("（", "）"), ("(", ")")):
        if left in s and right in s:
            head = s.split(left, 1)[0].strip()
            if head:
                s = head
                break
    return s


def classify_school(name: str | None) -> str:
    """根据学校名返回等级标签。

    返回值: '985' | '211' | 'qs_top200' | ''

    BUG-125: 中科院系统 (国科大 / 上海高研 / 深先进 / 自动化所等) 与
    国家级实验室 (深圳湾 / 鹏城 / 之江 / 上海AI) 视同 985 档.
    """
    s = _normalize(name)
    if not s:
        return ""

    canonical = _ALIASES.get(s, s)

    if canonical in SCHOOLS_985_EQUIV:
        return "985"
    if canonical in SCHOOLS_211:
        return "211"
    if canonical in SCHOOLS_QS_TOP200:
        return "qs_top200"

    # contains 兜底: 输入中文可能含"分校/校区"或机构后缀
    for known in SCHOOLS_985_EQUIV:
        if known in s:
            return "985"
    for known in SCHOOLS_211:
        if known in s:
            return "211"
    for known in SCHOOLS_QS_TOP200:
        if known in s:
            return "qs_top200"
    return ""


def tier_value(tier: str | None) -> int:
    return _TIER_VALUES.get(tier or "", 0)


def meets_school_tier(candidate_tier: str | None, job_min: str | None) -> bool:
    """候选人学校等级是否达岗位门槛"""
    return tier_value(candidate_tier) >= tier_value(job_min)


def education_value(edu: str | None) -> int:
    return _EDU_VALUES.get(edu or "", 0)


def meets_education(candidate_edu: str | None, job_min: str | None) -> bool:
    """候选人学历层级是否达岗位门槛"""
    return education_value(candidate_edu) >= education_value(job_min)
